public class Uttestprep {
		public static NString FsetupProgram() {
			
			IDataCommand cmd = DbManager.getDataAccessFactory().createDataCommand("UTTESTPREP.SETUP_PROGRAM", DbManager.getDataBaseFactory());
			
			cmd.addReturnParameter(NString.class);
				
			cmd.execute();

			return cmd.getReturnValue(NString.class);

		}
		
		public static NString FteardownProgram() {
			
			IDataCommand cmd = DbManager.getDataAccessFactory().createDataCommand("UTTESTPREP.TEARDOWN_PROGRAM", DbManager.getDataBaseFactory());
			
			cmd.addReturnParameter(NString.class);
				
			cmd.execute();

			return cmd.getReturnValue(NString.class);

		}
		
	
	
	
}
